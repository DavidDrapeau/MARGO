    <h3>
        Présentation de Margo:
    </h3>
    
    <p>
        MARGO est un organisme français de formation professionnelle.

        Les formations proposées sont sanctionnées par un Titre Professionnel, 
        délivré par le ministère de l'Emploi, et qui est reconnu par les employeurs.
    </p>
    <p>
        Ces titres professionnels sont également accessibles par la validation des acquis de l'expérience (VAE).
        Les formations de MARGO sont ouvertes aux demandeurs d'emploi et aux salariés en partenariat avec les organismes publics, 
        les collectivités territoriales, les entreprises (grandes, PME-PMI ou artisans). 
        Les contrats de professionnalisation font aussi partie du dispositif.
    </p>

